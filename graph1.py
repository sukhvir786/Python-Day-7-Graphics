import turtle
sh = turtle.Turtle() # let sh for shape
sh.showturtle()
sh.forward(200)
sh.left(90)
sh.forward(200)
sh.left(90)
sh.forward(200)
sh.left(90)
sh.forward(200)
turtle.done()
